import Foundation

/// A set of extensions to the Locale type.
public extension Locale {
    /// A static property that provides an English (United States) locale.
    ///
    /// This property returns a Locale instance configured for English (United States)
    /// which can be used for consistent localization for internal enum cases.
    static let enUS = Locale(identifier: "en_US")
}
